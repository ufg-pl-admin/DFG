#' redirectedPlot
#'
#' @description
#' Funkcja `redirectedPlot` wywołuje określoną funkcję wraz z danymi i zwraca wynik w postaci wykresu.
#' Metoda ta korzysta z funkcji `parse` i `eval`, aby zinterpretować przekazaną nazwę funkcji jako tekstowy kod,
#' który może być wykonany.
#'
#' @usage
#' redirectedPlot(functionName, data)
#'
#' @param functionName Nazwa funkcji do wywołania jako tekstowy ciąg znaków.
#' @param data Dane, na których funkcja będzie wykonywana.
#'
#' @return
#' Metoda zwraca wynik wywołania funkcji jako obiekt graficzny.
#'
#' @examples
#' # Wywołanie funkcji plot z danymi iris
#' redirectedPlot("plot", iris)
#'
#' # Wywołanie niestandardowej funkcji
#' myFunction <- function(data) {
#'   plot(data$col1, data$col2)
#' }
#' redirectedPlot("myFunction", myData)
#'
redirectedPlot <- function(functionName, data) {
    redirected_plot <- eval(parse(text=paste("INFOGRAPHIC_", functionName, "(data)", sep="")))
    return(redirected_plot)
}